# Function to show the content of a list
def show_list_content(lst):
    print("The content of the list is:", lst)

# Function to find the maximum value in a list
def find_max_value(lst):
    max_value = max(lst)
    print("The max value in the list:", max_value)

# Sample List
sample_list = [10, 2, 3, 4, 7]

# Show the content of the list
show_list_content(sample_list)

# Find the maximum value in the list
find_max_value(sample_list)
