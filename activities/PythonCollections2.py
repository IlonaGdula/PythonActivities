# Sample list
num_list = [20, 30, 25, 35, -16, 60, -100]

# Initialize variables
sum_value = 0
count = 0

# Calculate sum of list elements
for num in num_list:
    sum_value += num
    count += 1

# Calculate average
average = sum_value / count

# Print the average value
print("Average value of the list elements is:", average)
